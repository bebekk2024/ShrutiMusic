async def get_client(assistant: int):
    """
    Return the corresponding userbot client for assistant number.
    Returns None if assistant number is invalid.
    """
    try:
        a = int(assistant)
    except Exception:
        return None

    if a == 1:
        return userbot.one
    elif a == 2:
        return userbot.two
    elif a == 3:
        return userbot.three
    elif a == 4:
        return userbot.four
    elif a == 5:
        return userbot.five
    return None


